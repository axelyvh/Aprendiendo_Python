padres = ["mama", "papa"]
abuelos = ["abuelo", "abuela", "hijos"]
numeros = [1, 2, 3]

# SUMATORIA
familia = numeros + abuelos + padres
print(familia)

# MULTIPLICACION
lista_numeros = numeros * 2
print(lista_numeros)

# IN
print("mama" in padres)
print("prueba" in padres)

# NOT IN
print("mama" not in padres)
print("prueba" not in padres)