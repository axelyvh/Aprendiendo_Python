# + -> Adición
print("x+y: ", x + y)

# - -> Sustracción
print("x-y: ", x - y)

# * -> Multiplicación
print("x*y: ", x * y)

# / -> División
x, y = 13, 2
print("x/y: ", x / y)

# % -> Modulo
x, y = 13, 2
print("x%y: ", x % y)

# ** -> Exponenciación
x, y = 2, 2
print("x**y: ", x ** y)

# // -> Division entera
x, y = 13, 2
print("x//y: ", x // y)

